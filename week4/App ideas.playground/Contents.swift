 1.美食地圖\
這是要解決很多人找餐廳的困擾，一般的方法都是透過google map，距離、營業中、價位來判斷。
把朋友推薦的元素加入進去，用戶可以建立自己的美食地圖，和朋友分享，資訊包含照片、地點、價位、評論。
UI介面可以有兩種主要模式，一種是地圖模式，一種是列表模式;地圖模式，是把朋友推薦的餐廳呈現在地圖上，
用pin來註記，列表模式是，根據朋友列表，來閱覽朋友推薦的餐廳。
和傳統找餐廳的方式最大的不同在於，google map的星星數量，只能做大致的判斷，然後裡面其他用戶所分享的評論過於龐雜，很難找到關鍵資訊判斷是否餐廳適合自己，然後透過朋友推薦，人們更相信身邊朋友的味覺，這樣的推薦更有說服力。
 
 
 
 

 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
